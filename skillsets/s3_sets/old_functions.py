# Print set
def setPrint(setIn):
    print(setIn)

# Print set type
def setPrintTyp(setIn):
    print(type(setIn))

# Print length of set
def setPrintLen(setIn):
    print(len(setIn))

# Print set minimum value
def setPrintMin(setIn):
    print(min(setIn))

# Print set maximum value
def setPrintMax(setIn):
    print(max(setIn))

# Print sum of values of set
def setPrintSum(setIn):
    print(sum(setIn))